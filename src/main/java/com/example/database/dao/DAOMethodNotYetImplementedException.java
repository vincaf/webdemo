package com.example.database.dao;

public class DAOMethodNotYetImplementedException extends RuntimeException {

	private static final long serialVersionUID = 1L;

	public DAOMethodNotYetImplementedException() {
	}

	public DAOMethodNotYetImplementedException(String message) {
		super(message);
	}

	public DAOMethodNotYetImplementedException(Throwable cause) {
		super(cause);
	}

	public DAOMethodNotYetImplementedException(String message, Throwable cause) {
		super(message, cause);
	}

	public DAOMethodNotYetImplementedException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

}
